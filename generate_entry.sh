#!/bin/zsh
# ============================================================================
# クロスプラットフォーム日付計算関数 (macOS & Linux)
# ============================================================================
xplatform_date() {
    local input_date="$1"
    local modifier="$2" # 例: "+1d", "-1d"
    local output_format="$3"
    local input_format="%Y-%m-%d"

    if date --version >/dev/null 2>&1; then
        # GNU date (Linux)
        local days=${modifier%d} # e.g., "+1d" -> "+1"
        date -d "$input_date ${days} days" "+${output_format}"
    else
        # BSD date (macOS)
        date -j -v"${modifier}" -f "${input_format}" "${input_date}" "+${output_format}"
    fi
}

# ============================================================================
# 日記エントリ生成スクリプト
# ============================================================================
# このスクリプトは、最新の日記ファイルの次の日付のエントリを生成するための
# 各種変数を準備します。
# 
# 動作概要:
#   1. 最新の日記ファイル（????-??-??.md形式）を検索
#   2. そのファイルの日付の「次の日」を計算
#   3. その日付を元に、年、月、ディレクトリパス、前後の日付などを計算
#   4. 日本語形式のタイトル日付を生成
# ============================================================================

# ----------------------------------------------------------------------------
# 初期日付の設定（後で上書きされる可能性あり）
# ----------------------------------------------------------------------------
# ${1:-$(date "+%Y-%m-%d")} の意味:
#   - $1: スクリプトの第一引数（指定された日付）
#   - :- : パラメータ展開のデフォルト値指定演算子
#   - $(date "+%Y-%m-%d"): 引数がなければ今日の日付を取得
# 
# 例: ./generate_entry.sh 2025-04-15 → TODAY="2025-04-15"
#      ./generate_entry.sh            → TODAY="2026-01-03"（今日の日付）
# ----------------------------------------------------------------------------
TODAY=${1:-$(date "+%Y-%m-%d")}

# ----------------------------------------------------------------------------
# 最新の日記ファイルを検索
# ----------------------------------------------------------------------------
# find . -type f -name "????-??-??.md":
#   - . : カレントディレクトリから再帰的に検索
#   - -type f : ファイルのみ（ディレクトリは除外）
#   - -name "????-??-??.md": 日付形式のファイル名にマッチ
#     ? は任意の1文字（例: 2025-04-12.md, 2026-01-01.md）
# 
# | sort | tail -1:
#   - sort: ファイル名を辞書順（日付順）にソート
#   - tail -1: 最後の1行（最新の日付のファイル）を取得
# 
# 結果例: ./2026/01/2026-01-02.md
# ----------------------------------------------------------------------------
LATEST_FILE=$(find . -type f -name "????-??-??.md" | sort | tail -1)
echo "LATEST_FILE = \"$LATEST_FILE\""

# ----------------------------------------------------------------------------
# 最新ファイルの日付部分を抽出
# ----------------------------------------------------------------------------
# basename "$LATEST_FILE" .md の意味:
#   - basename: パスからファイル名部分だけを抽出
#   - "$LATEST_FILE": 例: ./2026/01/2026-01-02.md
#   - .md: 拡張子を除去
# 
# 結果例: 2026-01-02
# ----------------------------------------------------------------------------
LATEST_DATE=$(basename "$LATEST_FILE" .md)

# ----------------------------------------------------------------------------
# 最新ファイルの「次の日」をTODAYに設定（上書き）
# ----------------------------------------------------------------------------
if [[ -n "$LATEST_DATE" ]]; then
    TODAY=$(xplatform_date "$LATEST_DATE" "+1d" "%Y-%m-%d")
fi

# ============================================================================
# TODAYの値を元に、各種変数を計算
# ============================================================================

# ----------------------------------------------------------------------------
# 年（YEAR）を抽出
# ----------------------------------------------------------------------------
YEAR=$(xplatform_date "$TODAY" "+0d" "%Y")

# ----------------------------------------------------------------------------
# 月（MONTH）を抽出
# ----------------------------------------------------------------------------
MONTH=$(xplatform_date "$TODAY" "+0d" "%m")

# ----------------------------------------------------------------------------
# ターゲットディレクトリパスを生成
# ----------------------------------------------------------------------------
# 日記ファイルを保存するディレクトリパス
# 
# 結果例: 2026/01
# ----------------------------------------------------------------------------
TARGET_DIR="$YEAR/$MONTH"

# ----------------------------------------------------------------------------
# 昨日のファイルパスを生成
# ----------------------------------------------------------------------------
YESTERDAY_STR=$(xplatform_date "$TODAY" "-1d" "%Y/%m/%Y-%m-%d.md")

# ----------------------------------------------------------------------------
# 明日のファイルパスを生成
# ----------------------------------------------------------------------------
TOMORROW_STR=$(xplatform_date "$TODAY" "+1d" "%Y/%m/%Y-%m-%d.md")

# ----------------------------------------------------------------------------
# 日本語形式のタイトル日付を生成
# ----------------------------------------------------------------------------
# LC_ALL=ja_JP.UTF-8: ロケールを日本語に設定（曜日を日本語で表示するため）
# "+%Y年%m月%d日（%a）": 出力形式
#   - %Y: 年（4桁）
#   - %m: 月（2桁）
#   - %d: 日（2桁）
#   - %a: 曜日の短縮形（日本語ロケールで「月」「火」など）
# 
# 結果例: 2026年01月03日（金）
# ----------------------------------------------------------------------------
if [[ $(uname) == "Darwin" ]]; then
    TITLE_DATE=$(LC_ALL=ja_JP.UTF-8 date -j -f "%Y-%m-%d" "$TODAY" "+%Y年%m月%d日（%a）")
else
    TITLE_DATE=$(LC_ALL=ja_JP.UTF-8 date -d "$TODAY" "+%Y年%m月%d日（%a）")
fi

# ============================================================================
# デバッグ用: 計算された変数の内容を表示
# ============================================================================
# スクリプトの動作確認やトラブルシューティングのために、
# すべての変数の値を出力します。
# ============================================================================
echo "TODAY = \"$TODAY\" (指定された日付)"
echo "TITLE_DATE = \"$TITLE_DATE\""
echo "YEAR = \"$YEAR\""
echo "MONTH = \"$MONTH\""
echo "TARGET_DIR = \"$TARGET_DIR\""
echo "YESTERDAY_STR = \"$YESTERDAY_STR\""
echo "TOMORROW_STR = \"$TOMORROW_STR\""
